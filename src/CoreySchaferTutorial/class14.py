#virtualenv

'''

o virtualenv é uma ferramenta que permite criar ambientes Python isolados.

Isso significa que você pode ter diferentes versões do Python e pacotes instalados em diferentes ambientes, sem que um interfira no outro.

Isso é útil quando você está trabalhando em vários projetos que precisam de versões diferentes de bibliotecas ou do próprio Python.

Ao criar um ambiente virtual com o virtualenv, você pode instalar pacotes Python nele sem afetar o ambiente global do sistema.

Isso é útil porque você pode ter diferentes versões de pacotes em diferentes projetos, sem que um projeto afete o outro.

Para criar um novo ambiente virtual com virtualenv, você pode usar o seguinte comando no terminal:

'''

#   virtualenv nome-do-ambiente
 

#Depois de criar o ambiente virtual, você pode ativá-lo usando o comando:

#   nome-do-ambiente\Scripts\activate

#Para desativar o ambiente virtual :

#   deactivate
